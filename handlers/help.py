from aiogram import Router, F
from aiogram.filters import Command
from aiogram.types import Message

router = Router()


HELP_TEXT = """<b>❓ Справка по боту</b>

<b>Основные функции:</b>
💬 <b>Задать вопрос</b> — задай любой вопрос по воспитанию ребёнка
👶 <b>Расскажи о дочке</b> — советы с учётом текущего возраста
📖 <b>Моя библиотека</b> — управление книгами в базе знаний
👤 <b>Мой профиль</b> — личные данные о ребёнке

<b>Режимы поиска:</b>
📚 <b>Только из книг</b> — ответы строго из загруженной базы знаний
🌐 <b>Книги + интернет</b> — дополнительно ищет в интернете с проверкой источников

<b>Команды:</b>
/start — главное меню
/setdate — изменить дату рождения ребёнка
/myprofile — профиль ребёнка
/fillprofile — быстро заполнить профиль (серия вопросов)
/help — эта справка

<b>Загрузка книг:</b>
Отправь PDF-файл (до 20 MB) боту — он автоматически попадёт в базу знаний.
Нужно будет указать возрастной диапазон книги.

<b>Совет:</b> Чем больше информации ты добавишь в 👤 Мой профиль (имя, характер, особенности ребёнка), тем точнее будут ответы."""


@router.message(F.text == "❓ Помощь")
@router.message(Command("help"))
async def cmd_help(message: Message):
    await message.answer(HELP_TEXT)
